package day04.homework;

public class ForEx02 {

	public static void main(String[] args) {
		
		//1부터 5까지 콘솔에 출력하는 코드를 작성하세요.
		
		for (int i = 1; i < 6; i++) {
			System.out.print(i + " ");
		}
		System.out.println();
		
		//10부터 1까지 콘솔에 출력하는 코드를 작성하세요.
		
		for (int i = 10; i > 0; i--) {
			System.out.print(i + " ");
		}	
		System.out.println();
	}

}
