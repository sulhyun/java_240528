package day02;

public class StringEx01 {

	public static void main(String[] args) {
		
		// 참조형
		// String 클래스 : 문자열을 저장하는 클래스
		
		String str1 = null;
		String str2 = "안녕하세요.";
		String str3 = "null";
		String str4 = ""; // 문자열은 0자 이상
		
	
		//이 때 출력되는 null 은 값 
		System.out.println(str1); 
		
		System.out.println(str2);
		System.out.println(str4);
		
		// 이 때 출력되는 null 은 문자열
		System.out.println(str3);
	}

}
